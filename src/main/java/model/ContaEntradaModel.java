package model;

public class ContaEntradaModel {
	
	   	private String idendificador;
	    private String codigoC;
	    private String codigoD;
	    private String descricao;   
	    
		public ContaEntradaModel() {
			super();
		}
		public String getIdendificador() {
			return idendificador;
		}
		public void setIdendificador(String idendificador) {
			this.idendificador = idendificador;
		}
		public String getCodigoC() {
			return codigoC;
		}
		public void setCodigoC(String codigoC) {
			this.codigoC = codigoC;
		}
		public String getCodigoD() {
			return codigoD;
		}
		public void setCodigoD(String codigoD) {
			this.codigoD = codigoD;
		}
		public String getDescricao() {
			return descricao;
		}
		public void setDescricao(String descricao) {
			this.descricao = descricao;
		}
	    
	    

}
