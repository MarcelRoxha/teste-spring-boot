package models;

public class Usuario {
	
  private String  identificadorUsuaior;
  private String  nome;
  private String  nomeusuario;
  private String  email;
  private String  nivelAcesso;
  private String  created;
  private String  modified;
  private String  quantidadeEmpresasVinculada;
  
  
  
  
public Usuario() {
	super();
}
public String getIdentificadorUsuaior() {
	return identificadorUsuaior;
}
public void setIdentificadorUsuaior(String identificadorUsuaior) {
	this.identificadorUsuaior = identificadorUsuaior;
}
public String getNome() {
	return nome;
}
public void setNome(String nome) {
	this.nome = nome;
}
public String getNomeusuario() {
	return nomeusuario;
}
public void setNomeusuario(String nomeusuario) {
	this.nomeusuario = nomeusuario;
}
public String getEmail() {
	return email;
}
public void setEmail(String email) {
	this.email = email;
}
public String getNivelAcesso() {
	return nivelAcesso;
}
public void setNivelAcesso(String nivelAcesso) {
	this.nivelAcesso = nivelAcesso;
}
public String getCreated() {
	return created;
}
public void setCreated(String created) {
	this.created = created;
}
public String getModified() {
	return modified;
}
public void setModified(String modified) {
	this.modified = modified;
}
public String getQuantidadeEmpresasVinculada() {
	return quantidadeEmpresasVinculada;
}
public void setQuantidadeEmpresasVinculada(String quantidadeEmpresasVinculada) {
	this.quantidadeEmpresasVinculada = quantidadeEmpresasVinculada;
}
  
  

}
